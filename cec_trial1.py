def get_true_wind_angle(target_date, target_lat, target_lon):
    """
    Queries the ERA5 dataset for a specific time and location,
    and calculates the meteorological wind direction (0-360 degrees).
    """
    try:
        # THE FIX: Changed 'time' to 'valid_time' to match the new Copernicus format
        point_data = weather_ds.sel(
            valid_time=target_date,
            latitude=target_lat,
            longitude=target_lon,
            method="nearest"
        )

        # Extract the U and V vector components
        u_vector = float(point_data['u10'].values)
        v_vector = float(point_data['v10'].values)

        # Calculate Meteorological Wind Direction
        direction = (270 - np.rad2deg(np.arctan2(v_vector, u_vector))) % 360

        # Calculate Wind Speed
        speed = np.sqrt(u_vector**2 + v_vector**2)

        return direction, speed

    except KeyError as e:
        print(f"Error: Could not find exact time/coordinates. Details: {e}")
        return None, None

