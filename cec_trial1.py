def calculate_sea_percentage(filepath):
    """Reads the image at 10% resolution to check for sea pixels."""
    try:
        with rasterio.open(filepath) as src:
            # Read at 1/10th scale to save RAM
            data = src.read(1, out_shape=(1, int(src.height // 10), int(src.width // 10)))

            # Assuming sea pixels are valid data (>0) and not NaNs
            valid_sea_mask = (data > 0.0) & ~np.isnan(data)

            sea_pixels = np.sum(valid_sea_mask)
            total_pixels = data.size

            return (sea_pixels / total_pixels) * 100
    except Exception as e:
        print(f"Error reading {os.path.basename(filepath)}: {e}")
        return 0.0

