class WindDataGenerator(tf.keras.utils.Sequence):
    def __init__(self, base_x, base_y, batch_size=16):
        # A base batch of 16 will yield an augmented batch of 96 to the GPU
        self.base_x = base_x
        self.base_y = base_y
        self.batch_size = batch_size
        self.indices = np.arange(len(self.base_x))
        np.random.shuffle(self.indices)

    def __len__(self):
        return int(np.ceil(len(self.base_x) / self.batch_size))

    def __getitem__(self, index):
        batch_idx = self.indices[index * self.batch_size : (index + 1) * self.batch_size]
        batch_x = self.base_x[batch_idx]
        batch_y = self.base_y[batch_idx]

        aug_x, aug_y = [], []
        for patch, angle in zip(batch_x, batch_y):
            p_exp = patch[..., np.newaxis]
            aug_x.append(p_exp)
            aug_y.append(angle)
            for k in [1, 2, 3]:
                aug_x.append(np.rot90(p_exp, k=k))
                aug_y.append((angle - 90 * k) % 360)
            aug_x.append(np.fliplr(p_exp))
            aug_y.append((360 - angle) % 360)
            aug_x.append(np.flipud(p_exp))
            aug_y.append((180 - angle) % 360)

        aug_x = np.array(aug_x, dtype=np.float32)
        aug_y = np.array(aug_y, dtype=np.float32)
        shuff_idx = np.arange(len(aug_x))
        np.random.shuffle(shuff_idx)

        return aug_x[shuff_idx], aug_y[shuff_idx]

    def on_epoch_end(self):
        np.random.shuffle(self.indices)

